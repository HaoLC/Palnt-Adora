/// [ButtonPosition] is used to position the icon, badges to start or end of the button
/// See ButtonButton and ButtonButtonBadge
enum DefaultPosition {
  /// [ButtonPosition.start] is used to place icon, badges to start of the ButtonButton and ButtonButtonBadge
  start,

  /// [ButtonPosition.end] is used to place icon, badges to end of the ButtonButton and ButtonButtonBadge
  end,
}