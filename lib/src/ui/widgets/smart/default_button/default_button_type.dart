/// [DefaultButtonType] is used to change the type of widgets
enum DefaultButtonType {
  /// Default type is [DefaultButtonType.solid], used to fill with color for widget
  solid,

  /// Type [DefaultButtonType.outline], used for widget with outline border and fill color with Colors.transparent
  outline,

  /// Type [DefaultButtonType.outline2x], used for widget with outline2x border and border.width = 2.0 and fill color with Colors.transparent
  outline2x,

  /// Type [DefaultButtonType.transparent], used for widget with fill color with Colors.transparent
  transparent,
}