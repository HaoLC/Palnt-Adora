/// [DefaultSize] is used to change the size of the widget.
class DefaultSize {
  /// [DefaultSize.SMALL] is used for small size widget
  static const double SMALL = 30;

  /// Default size if [DefaultSize.MEDIUM] is used for medium size widget
  static const double MEDIUM = 35;

  /// [DefaultSize.LARGE] is used for large size widget
  static const double LARGE = 40;
}
